package day03_Variables;

public class EscapeSequences {

    public static void main(String[] args) {

        System.out.println("Hello Everyone");
        System.out.println("My name is Ruzanna");
        System.out.println("I am from Russia");

        System.out.println("======================================");

        System.out.println("Hello Everyone\nMy name is Ruzanna, I am from Russia");


        //\t:
        System.out.println("\ttoday in learned Java");

        // \\:  \
        System.out.println("/"); // prints single front slash
        System.out.println("\\"); // prints single back slash

        System.out.println("======================================");

        System.out.println("My name is 'Ruzanna'");
        System.out.println("My name is \'Ruzanna\'");

        System.out.println("======================================");
        // \":
        System.out.println("My favourite TV Series is \"Gossip Girl\"");



    }



}
