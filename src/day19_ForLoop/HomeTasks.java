public class HomeTasks {

    public static void main(String[] args) {

        for (char i = 97; i <=122 ; i++) {
            System.out.print(i+" ");
        }
        System.out.println();
        System.out.println("==========================");

        for (char i = 65; i <=90 ; i++) {
            System.out.print(i+" ");
        }

        System.out.println();
        System.out.println("==========================");

        for (int i = 0; i <=30000 ; i++) {
            System.out.print(i+" ");
        }


    }
}
